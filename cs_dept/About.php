    <p>Pondicherry University is located in a campus spread over around 1,000 acres (400 ha), facing the Bay of Bengal on the East Coast Road. It is accessible from Chennai (168 km [104 mi]) and Bengaluru (326 km [203 mi]).

Hostel facilities are available on the campus for both males and females. Students from distant places will be given preference on a first-come, first-served basis. The university has a hostel for foreign students that can accommodate more than 30 students. Girls are given rent free accommodation. All physically challenged students get free education including free board and lodge in hostels. The university provides free transport off the campus and within the campus. The university which was B grade for 20 years, took a big leap forward during the period 2007 -2012 the 11 plan when prof J A K Tareen was vice chancellor. It grew by 300% all round including infrastructure, facilities and departments. Over 300 new faculty and 22 new departments were started and new campuses at Karaikal and Andaman. It became A grade and In the first NIRF ranking came to top 11 of Indian universities.Prof Tareen was an acclaimed institution builder.</p>
